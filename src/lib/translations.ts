type WidenLiteral<T> = T extends string
  ? string
  : T extends Array<infer U>
    ? Array<WidenLiteral<U>>
    : T extends Record<string, unknown>
      ? { [K in keyof T]: WidenLiteral<T[K]> }
      : T;

export type SiteTranslations = WidenLiteral<(typeof baseTranslations)['en']>;

export const translations = baseTranslations as unknown as Record<Lang, SiteTranslations>;

export function getTranslations(lang: Lang): SiteTranslations {
  return translations[lang];
}
